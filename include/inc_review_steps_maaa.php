<h2 class="ui header">1st Year Reviews</h2>
<h4 class="ui horizontal header divider"><i class="bar chart icon"></i>Specifications</h4>
<p>Sign up for a Behance.net account to use for putting together the materials for the first portfolio review.</p>
<h3 class="ui header"><div class="content">Behance Portfolios</div></h3>
<p>A <a href="http://www.behance.net/" target="_blank">Behance.net</a> portfolio gives you exposure to an Online network of creative professionals, and can be used by career services to promote you to employers when you graduate. Currently this is not a substitute for having a website to send to potential employers, but it is a good addition to your professional Online presence. If you have a Creative Cloud (Adobe) account, you already have a Behance portfolio through the Creative Cloud. The Art Institute has a  Portfolio gallery where you can add your Behance portfolio to a network of students in the Art Institute nationwide. Unless you already have a Behance portfolio, you can start to build a behance.net portfolio by  going to <a href="http://portfolios.artinstitutes.edu/" target="_blank">portfolios.artinstitutes.edu</a> and start your portfolio through the AI network. After opening the AI gallery,  choose the <a href="http://portfolios.artinstitutes.edu/signup/authenticate" target="_blank">Sign up and Showcase Your Work</a> link. If you already have a behance portfolio, you can go to <a href="http://portfolios.artinstitutes.edu/" target="_blank">portfolios.artinstitutes.edu</a> page and login to add your existing portfolio to the network.</p>
<p>When creating a Behance portfolio, your portfolio can be accessed directly using the http://www.behance.net/username, or http://be.net/username, where your own unique username will be added after the Behance address. So when creating your username make sure you create a username that is professional an meaningful.</p>
<p>After you have created a Behance profile, </p>
<ul>
  <li>Minimum of 12 picies of artwork. Begin with your best piece. Open up your review with your best work!</li>
  <li>You have only one chance to make a good first impression. </li>
  <li>The first third (1/3) of the portfolio is an area of which you wish to focus.</li>
  <li>The second third (2/3) area is supporting projects.</li>
  <li>The final third (3/3) are anatomy projects.
    <ul>
      <li><strong>Hands, Feet, Faces, Anatomy, understanding of Perspective are key areas of close scrutiny.</strong></li>
    </ul>
  </li>
  
  <div class="ui info message">
  <div class="header">
    Was this what you wanted?
  </div>
  <ul class="list">
    <li>It's good to see you again.</li>
    <li>Did you know it's been a while?</li>
  </ul>
</div>
  
  <li>Revisit/adjust/redo any work as needed to demonstrate your skills in the Foundations, Storytelling and Color. <em>It&rsquo;s amazing what a few tweaks in Photoshop can do to improve older artwork.</em></li>
  <li>SPELL AND GRAMMAR CHECK everything!!! Get more eyes on your work to catch the typos.</li>
  <li>Use high rez images: Balance between appearance (no jaggies or dithering please) and performance.</li>
  <li><strong>Remove</strong> any Kung Fu panda work.  Our goal is to keep all your artwork as original as possible.</li>
  <li>Any drawings of actors/famous models/actresses should be credited. If you did not know the name of the person, please replace with other original art. </li>
  <li>Photo Manipulation &amp; Digital ink and paint projects: If you have used photo reference or taken parts from any photos…<em>please include the photo(s) so we can see you work/process.</em></li>
  <li>You are welcomed to reduce and make swatches of the anatomy studies to consolidate the pages.  Be sure to include gesture images/sequential poses too! (SLO #3).</li>
  <li><strong>Remove</strong> any Bridgman, Loomis and other anatomy research studies. While they are fantastic for the sketchbook…please avoid using for any review or portfolio.</li>
  <li>Continue to push the &ldquo;Story&rdquo; and the essence of life in your art work. This will directly assist with the SLO#3 on the MAA/GAD Rubric.</li>
  <li><strong>Guide the viewer </strong>through your work using titles and brief concise descriptions along the way. You may know what the artwork is…but other viewers may not.</li>
  <li>Check and recheck the links to ensure they work. Clearly label all external links so the user will know what to click on to view the animations/clips.</li>
  <li>Think of CRAFTSMANSHIP in all areas.  Revisit the artwork. Sloppy, rushed work and/or poor display of your art will result in failing the review.  <em>Show that you care.</em></li>
  <li><strong>Review your portfolio and get feedback!</strong> Experiment with moving around images for a better &ldquo;flow&rdquo; of the overall work being displayed. <strong>What impression do you want the reviewer to walk away with?</strong></li>
</ul>
